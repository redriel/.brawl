# Object class for a fighter

class Fighter:
    def __init__(self, name, description, stamina, punchforce, guts, tecnique, nimble, tough, philosophy):
        self.name = name
        self.description = description
        self.stamina = stamina
        self.punchforce = punchforce
        self.guts = guts
        self.tecnique = tecnique
        self.nimble = nimble
        self.tough = tough
        self.philosophy = philosophy